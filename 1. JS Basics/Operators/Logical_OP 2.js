// !(NOT) operator 
let a = 1; 
console.log(!a); // Result: false


// &&(AND) operator 
console.log( 0 && 1 ); // 0 
console.log( 1 && 3 ); // 3 
console.log( null && true ); // null 
console.log( 1 && 2 && 3 && 4); // 4

// ||(OR) Operator 
console.log( 0 || 1 ); // 1 
console.log( 1 || 3 ); // 1 
console.log( null || true ); // true 
console.log( -1 || -2 || -3 || -4); // -1
