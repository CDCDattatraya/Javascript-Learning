// Unary Operator or Increment Decrement Operator
let a = 5;
let b = 2;

// a++ => a = a+1 Increment by 1
// a-- => a = a-1 Decrement by 1

console.log("Unary Operator")
console.log('a = 5 and b = 2')
console.log('In a++ the value of a printed and then incremented by 1 => a++', a++); // Result: 5 then it is incremented by 1  
console.log('In ++b the value of b incremented and then printed by 1 => ++b', ++b); // Result: 3 