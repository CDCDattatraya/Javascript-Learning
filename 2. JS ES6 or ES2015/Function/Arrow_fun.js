// Arrow function 
let bolbhai = (name) => {
        return `Hello, ${name}!`;
    };
console.log(bolbhai('datta said')); // Output: 'Hello, Bhawa!'



// Shortened arrow function or single line function
let add = (a, b) => a + b;
console.log(add(34, 3)); // Output: 37

let cube = x => x**3; // For single argument
console.log(cube(7));