// Static Methods
class MathOperations {
        static add(x, y) {
          return x + y;
        }
      
        static subtract(x, y) {
          return x - y;
        }
      }
      
      console.log(MathOperations.add(5, 3));      // Output: 8
      console.log(MathOperations.subtract(5, 3)); // Output: 2